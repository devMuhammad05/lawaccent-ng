                                    <li>Requires a minimum of two shareholders.</li>
                                </ul>
                            </li>
                            <li>
                                Incorporated Trustees (NGOs, Foundations, Religious Bodies)
                                <ul>
                                    <li>Best for non-profit organizations.</li>
                                </ul>
                            </li>
